//1. příklad
console.log('První příklad - vzestupná řada 0 - 10');
//WHILE
let i = 0;
while (i <= 10) {
    console.log(i);
    i++;
}
console.log('-------------------');

// 2. příklad - zkopíruj předchozí příklad
// a vyzkoušej, jaký je rozdíl mezi <= a <
// v podmínce uvnitř cyklu

console.log('Druhý příklad - rozdíl mezi <= a <');

//TADY NAPIS CYKLUS
//WHILE
i = -1;
while (i < 10) {
    i++;
    console.log(i);
}

console.log('-------------------');

//3. příklad
console.log('Třetí příklad - sestupná řada 10 - 0');

//TADY NAPIS CYKLUS
//WHILE
i = 10;
while (i >= 0) {
    console.log(i);
    i--;
}

console.log('-------------------');